/* רינדור בלוקי התשובה (משותף לכל השאלות) */
export function FaqAnswer({ blocks }: { blocks: FaqBlock[] }) {
  return (
    <>
      {blocks.map((b, i) => {
        if (b.t === "ul") {
          return (
            <ul key={i}>
              {b.items.map((it, j) => (
                <li key={j}>{it}</li>
              ))}
            </ul>
          );
        }
        if (b.t === "note") {
          return (
            <div key={i} className="faq-note">
              {b.c}
            </div>
          );
        }
        if (b.t === "muted") {
          return (
            <p key={i} className="faq-muted">
              {b.c}
            </p>
          );
        }
        return <p key={i}>{b.c}</p>;
      })}
    </>
  );
}

/* חילוץ טקסט גולמי מ-ReactNode — עבור JSON-LD (בלי לשכפל את התוכן) */
function nodeText(n: ReactNode): string {
  if (n == null || typeof n === "boolean") return "";
  if (typeof n === "string" || typeof n === "number") return String(n);
  if (Array.isArray(n)) return n.map(nodeText).join("");
  if (typeof n === "object" && "props" in n) {
    return nodeText((n as { props?: { children?: ReactNode } }).props?.children);
  }
  return "";
}

function blocksToText(blocks: FaqBlock[]): string {
  return blocks
    .map((b) => (b.t === "ul" ? b.items.map(nodeText).join(". ") : nodeText(b.c)))
    .join(" ")
    .replace(/\s+/g, " ")
    .trim();
}

/* JSON-LD FAQPage — רק שאלות ותשובות המרונדרות בפועל בעמוד */
export function buildFaqJsonLd() {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: FAQ_CATEGORIES.flatMap((cat) =>
      cat.items.map((item) => ({
        "@type": "Question",
        name: item.q,
        acceptedAnswer: { "@type": "Answer", text: blocksToText(item.a) },
      }))
    ),
  };
}
